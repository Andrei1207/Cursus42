#include "ft_printf.h"

void	ft_putstr(char *s)
{
	while (*s)
		ft_putchar((int)*s++);
}

int	ft_printstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		ft_putstr("(null)");
		return (6);
	}
	ft_putstr(s);
	while (s[i] != '\0')
		i++;
	return (i);
}

static int	ft_ptr_len(uintptr_t num)
{
	int	len;

	len = 0;
	if (num == 0)
		return (1);
	while (num != 0)
	{
		len++;
		num = num / 16;
	}
	return (len);
}

static void	ft_put_ptr(uintptr_t num)
{
	if (num >= 16)
	{
		ft_put_ptr(num / 16);
		ft_put_ptr(num % 16);
	}
	else
	{
		if (num <= 9)
			ft_putchar(num + '0');
		else
			ft_putchar(num - 10 + 'a');
	}
}

int	ft_ptr(unsigned long long ptr)
{
	int	print_length;

	print_length = 0;
	if (ptr == 0)
		return (print_length += write(1, "(nil)", 5));
	print_length += write(1, "0x", 2);
	ft_put_ptr(ptr);
	print_length += ft_ptr_len(ptr);
	return (print_length);
}
