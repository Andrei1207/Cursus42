char *ft_strrchr(const char *s, int c)
{
    int i;
    
    i = 0;
    while(*s)
    {
        if(s[i] == c)
            i++;
    }
    if(i >= 0)
    {
        return s[i];
    }
    else
    {
        return 0;
    }
}